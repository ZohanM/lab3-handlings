int pinX=28;
int pinY=130;
int hrArmLength=7;
int minArmLength=14;

void ST7735_drawHand(int time, int draw){
	//- because CW, /60 to ignore second, +90 to start at 0
	double minAng=-(((time/60)%60)*6)+90;//%60 bc only min
	double hrAng=-(((time/60)%(12*60))/2)+90;//%720 bc 12 hrs, 60min/hr, /2 bc 24 hr format
	
	//Cos&Sin require double
	double hrX = hrArmLength*cos((PI*hrAng)/180);
	double hrY = hrArmLength*sin((PI*hrAng)/180);
	
	double minX = minArmLength*cos(PI*minAng/180);
	double minY = minArmLength*sin(PI*minAng/180);
	
	if(draw){
		//Draw Hour
		ST7735_Draw4Lines(pinX, pinY, pinX+(int)hrX, pinY -(int)hrY, ST7735_RED);
		//Draw Min
		ST7735_Draw4Lines(pinX, pinY, pinX+(int)minX, pinY -(int)minY, ST7735_BLUE);
	}
	
	else{
		//Erase Hour
		ST7735_Draw4Lines(pinX, pinY, pinX+(int)hrX, pinY-(int)hrY, ST7735_BLACK);
		//Erase Min
		ST7735_Draw4Lines(pinX, pinY, pinX+(int)minX, pinY-(int)minY, ST7735_BLACK);
		//Redraw clock
		ST7735_drawClockFace();
	}
}

